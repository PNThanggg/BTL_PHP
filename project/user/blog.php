<script>
    $(document).ready(function() {
        var stt = 0;
        starImg = $("div.card-slide:first").attr("stt");
        endImg = $("div.card-slide:last").attr("stt");
        $("div.card-slide").each(function() {
            if ($(this).is(':visible'))
                stt = $(this).attr("stt");
        });
        $("#next").click(function() {
            if (stt == endImg) {
                stt = -1;
            }
            next = ++stt;
            $("div.card-slide").hide();
            $("div.card-slide").eq(next).show();
        });
        $("#prev").click(function() {
            if (stt == 0) {
                stt = endImg;
                prev = stt++;
            }
            prev = --stt;
            $("div.card-slide").hide();
            $("div.card-slide").eq(prev).show();
        });
    });
</script>